<p style="font-weight: 700;">Da wir die Igelstation ausschließlich ehrenamtlich betreiben, sind wir nicht immer sofort erreichbar. In diesem Falle bitte eine Nachricht hinterlassen. In dringenden Fällen, die Sofortmaßnahmen erfordern, bitte eine Igelstation / Pflegestelle über unsere <a href="?page=pflegestellen">Pflegestellen-Links</a> suchen und in der Zwischenzeit unsere Ratschläge zur <a href="?page=erste-hilfe"><span style="color: red;">✚</span>&nbsp;Ersten Hilfe</a> zu Rate ziehen.</p>

<h2>Was ist bei Igelfunden zu beachten?</h2>
<p>
	<img style="float: right; margin-left: 2em; max-width: 40%; height: auto;" alt="Erwin" title="Erwin" src="media/2018-10-06-08-32-50-7972.jpg" width="810" height="540">
	Nach § 44 Bundes&shy;naturschutz&shy;gesetz darf ein Igel als Vertreter einer besonders geschützten Art nicht aus der Natur entnommen werden. Ausnahme bilden nach § 45 ebd. kranke, verletzte oder hilflose Tiere.
</p>

<h2><a id="hilflos"></a>Wann ist ein Igel hilflos?</h2>

<p>Nicht alle Fundigel benötigen Hilfe. Bitte die Checkliste durchgehen um festzustellen, ob es sich wirklich um einen Notfall handelt!</p>

<p>Ist eine der folgenden Bedingungen gegeben, ist der Igel als hilfsbedürftig anzusehen.
	<ul>
		<li>Igel ist verletzt!</li>
		<li>Igel ist kälter als handwarm → Bereits auf dem Transport zur Igelstation aufwärmen (Wärmekissen <span style="font-style: italic">handwarm</span>), eine Behandlung kann erst bei Erreichen der normalen Körpertemperatur von 36°C erfolgen!</li>
		<li>Igel rollt sich bei Berührung nicht ein, ist apathisch</li>
		<li>Igel hat schlitzförmige, kaum sichtbare Augen</li>
		<li>Igel läuft orientierungslos im Kreis</li>
		<li>Igelbaby wurde ohne Muttertier aufgefunden → Gegend auf weitere Tiere absuchen!</li>
		<li>Igel irrt bei geschlossener Schneedecke / Eis umher</li>
		<li>Igel hat Ende November ein Gewicht von weniger als 500g</li>
	</ul>

Ansonsten kommt das Tier vermutlich allein zurecht und muss an Ort und Stelle verbleiben.
</p>

<h2><a id="infos"></a>Welche Informationen benötigt die Igelstation?</h2>

<p>Um die Situation des Tieres beurteilen und ggf. adäquate Maßnahmen bereits vor der Aufnahme ergreifen zu können, benötigt die Igelstation wichtige Daten zur Fundsituation und Zustand des Igels:
	<ul>
		<li>Ist der Igel verletzt?</li>
		<li>Ist der Igel warm?</li>
		<li>Wann wurde der Igel gefunden? Datum? Tageszeit?</li>
		<li>Wie wurde der Igel gefunden? Wurde der Igel aufgestört? Hat er offen herumgelegen?</li>
		<li>Säugling? → Weiße oder braune Stacheln? Augen und Ohren bereits geöffnet?</li>
		<li>Wir groß/schwer ist das Tier? → In einer Schüssel mit der Küchenwaage wiegen (Gewicht der Schüssel abziehen!). Größe kann auch verglichen werden mit Zitrone, Orange, Grapefruit, …</li>
		<li>Rollt sich der Igel ein?</li>
		<li>War das Tier bereits in menschlicher Obhut? Was wurde gefüttert?</li>
		<li>Wurde das Tier bereits tierärztlich behandelt?</li>
	</ul>
</p>
