<h1>Igel aufrollen</h1>

<p>Soll ein Igel auf Verletzungen, Fliegeneier oder Madenbefall untersucht werden,
muss er zuerst aufgerollt werden. Nur so können Bauch und Extremitäten inspiziert
werden. Dies ist wichtig, da Fliegeneier gerne in das sehr dichte Bauchfell
abgelegt werden und Maden sich gern in den Achselhöhlen verstecken.</p>

