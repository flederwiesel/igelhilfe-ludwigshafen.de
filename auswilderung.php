<h1>Auswilderung</h1>

<p>Da nicht alle Finder die Igel wieder auswildern können
oder wollen, wären wir dankbar, wenn uns ehrliche, zuverlässige
Igelfreunde unterstützen wollen. Optimal wäre ein eigener, offener
Garten mit Anschluss zu begrünten Flächen, in dem die Schnuffel
freigelassen werden können.</p>

<p>Alle wichtigen Informationen zur Auswilderung haben wir einem Flyer
 zusammengefasst, der auch als PDF heruntergeladen werden kann.</p>

