<h1>Biologie</h1>

<p>Igel fühlen sich in kleinräumigen, durchgrünten Gebieten
mit Hecken, Gebüsch und bodennahen Gewächsen wohl. Sie sind in Gärten,
Parks und Siedlungsrandgebieten zu Hause.</p>

