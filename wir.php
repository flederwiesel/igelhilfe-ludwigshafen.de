<h1>Über uns</h1>

<p>Die Igelhilfe Ludwigshafen ist ein 2020 gegründeter, als
gemeinnützig anerkannter Verein, der sich der Aufklärungsarbeit und
Förderung von Lebensraum für Igel widmet. Wir betreiben eine amtlich
genehmigte Auffangstation zur Pflege kranker, verletzter und hilfloser
Igel mit dem Ziel, diese baldmöglichst der Natur wieder zuzuführen.</p>

<p>Wir arbeiten mit dem <a href="https://tierheim-ludwigshafen.com/">Tierheim Ludwigshafen</a>,
den Tierhilfen der Region sowie anderen Igelpflegern und -stationen, bspw. den
<a href="https://igelfreunde-rp.de/">Igelfreunden Rhein-Pfalz</a> zusammen.</p>

