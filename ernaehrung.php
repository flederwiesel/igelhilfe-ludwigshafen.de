<h1>Ernährung</h1>

<p>Der Igel ist ein nachtaktiver <span style="font-weight: bold;">Insektenfresser</span>.
 Seine Hauptkost besteht aus Laufkäfern, Käfer- und
Schmetterlingslarven, Regenwürmern. Auch Schnecken, Spinnen, sowie
Hundert- und Tausendfüßler stehen auf seinem Speiseplan. Der Verzehr von
Obst &ndash; insbesondere Äpfel &ndash; gehört in das Reich der Märchen, auch sich
das noch immer nicht bei den Herstellern von industriellem Igelfutter
herumgesprochen hat… Es kommt allerdings häufig vor, dass Igel an
Fallobst gesichtet werden &ndash; dies allerdings nur wegen der Würmer.</p>

<p>Ersatzweise können Igel mit Katzenfutter (nass und trocken)
gefüttert werden, wobei hier ein Fleischanteil von &gt; 60% sowie ein
<span style="font-weight: bold;">hoher Protein- und Fettgehalt</span>
gegeben sein sollte. Auch gegartes <span style="font-weight: bold;">Rinder</span>hack
oder Rührei eignen sich gut zum Zufüttern. Bei Fütterung über einen
längeren Zeitraum sollten Ballaststoffe zugegeben werden.</p>

<p><span style="font-weight: bold;">Auf keinen Fall</span>
darf Igeln <span style="font-weight: bold;">Kuhmilch</span>
gegeben werden! Da Igel laktoseintolerant sind, führt dies zu schweren
Durchfällen! Sind auf Fotos Igelpfleglinge zu sehen, die Milch
schlabbern, ist dies spezielle, laktosearme Welpenmilch.</p>

